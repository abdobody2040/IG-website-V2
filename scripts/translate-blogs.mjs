async function main() {
  console.log('🔐 Authenticating as admin...')
  let token = ''

  let authRes = await fetch(`${PB_URL}/api/collections/_superusers/auth-with-password`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ identity: ADMIN_EMAIL, password: ADMIN_PASSWORD }),
  })

  if (authRes.ok) {
    token = (await authRes.json()).token
  } else {
    authRes = await fetch(`${PB_URL}/api/admins/auth-with-password`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ identity: ADMIN_EMAIL, password: ADMIN_PASSWORD }),
    })
    if (authRes.ok) {
      token = (await authRes.json()).token
    } else {
      console.error('❌ Failed to authenticate:', await authRes.text())
      process.exit(1)
    }
  }
  console.log('✅ Authenticated.')

  // Fetch all existing blogs to map their slugs to the IDs
  const searchRes = await fetch(`${PB_URL}/api/collections/blogs/records?perPage=100`, {
    headers: { Authorization: `Bearer ${token}` }
  })
  const searchData = await searchRes.json()

  console.log(`Checking ${searchData.items.length} blogs in PocketBase...`)

  let updatedCount = 0
  for (const item of searchData.items) {
    const slug = item.slug
    const translation = translations[slug]
    if (translation) {
      console.log(`Updating translation for ${slug}...`)
      const patchRes = await fetch(`${PB_URL}/api/collections/blogs/records/${item.id}`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
          Authorization: `Bearer ${token}`
        },
        body: JSON.stringify({
          title_ar: translation.titleAr,
          slug_ar: translation.slugAr,
          excerpt_ar: translation.excerptAr,
          content_ar: translation.contentAr
        })
      })
      if (patchRes.ok) {
        console.log(`✅ Successfully updated ${slug}`)
        updatedCount++
      } else {
        console.error(`❌ Failed to update ${slug}:`, await patchRes.text())
      }
    } else {
      console.warn(`⚠️ No translation map defined for slug: ${slug}`)
    }
  }

  console.log(`🏆 Done! Successfully updated ${updatedCount} blogs in Arabic.`)
}

main().catch(console.error)
