async function main() {
  console.log('🔐 Authenticating as admin...');
  let token = '';

  let authRes = await fetch(`${PB_URL}/api/collections/_superusers/auth-with-password`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ identity: ADMIN_EMAIL, password: ADMIN_PASSWORD }),
  });

  if (authRes.ok) {
    token = (await authRes.json()).token;
  } else {
    authRes = await fetch(`${PB_URL}/api/admins/auth-with-password`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ identity: ADMIN_EMAIL, password: ADMIN_PASSWORD }),
    });
    if (authRes.ok) {
      token = (await authRes.json()).token;
    } else {
      console.error('❌ Failed to authenticate:', await authRes.text());
      process.exit(1);
    }
  }

  console.log('✅ Authenticated successfully.');

  for (const svc of allServices) {
    console.log(`Seeding service: ${svc.title_en} (${svc.type})...`);
    
    // Check if exists
    let existRes = await fetch(`${PB_URL}/api/collections/services/records/${svc.id}`, {
      headers: { 'Authorization': `Bearer ${token}` }
    });

    let res;
    if (existRes.ok) {
      // Update
      res = await fetch(`${PB_URL}/api/collections/services/records/${svc.id}`, {
        method: 'PATCH',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify(svc)
      });
    } else {
      // Create
      res = await fetch(`${PB_URL}/api/collections/services/records`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify(svc)
      });
    }

    if (res.ok) {
      console.log(`✅ Successfully seeded: ${svc.title_en}`);
    } else {
      console.error(`❌ Failed to seed: ${svc.title_en}`, await res.text());
    }
  }

  console.log('\n🏆 Services Seeding Completed!');
}

main();
